package member.model;

public class MemberBean {
	private int no;
	private String id;
	private String pw;
	private String name;
	private String address;
	private int phone;
	private int cn1;
	private int cn2;
	private String signup_day;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getPhone() {
		return phone;
	}
	public void setPhone(int phone) {
		this.phone = phone;
	}
	public int getCn1() {
		return cn1;
	}
	public void setCn1(int cn1) {
		this.cn1 = cn1;
	}
	public int getCn2() {
		return cn2;
	}
	public void setCn2(int cn2) {
		this.cn2 = cn2;
	}
	public String getSignup_day() {
		return signup_day;
	}
	public void setSignup_day(String signup_day) {
		this.signup_day = signup_day;
	}
	
	
}
